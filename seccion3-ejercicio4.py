#Dados dos numeros, mostrar la suma, resta, división y multiplicación de ambos

numero1 = float(input("Indique el primer numero para realizar los calculos :"))
numero2 = float(input("Indique el segundo numero para realizar los calculos:"))

print("El resultado de la suma es          : ", numero1 + numero2)
print("El resultado de la resta es         : ", numero1 - numero2)
print("El resultado de la multiplicacion es: ", numero1 * numero2)
print("El resultado de la división es      : ", numero1 / numero2)
